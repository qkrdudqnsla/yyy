package www.deadlock.model.survey;

public class SurveyItemDTO {
	private int surveyitemnum;
	private String gender;
	private int num;
	private String age;
	private String content;
	private int count;
	private String id;
	
	
	
	
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public int getSurveyitemnum() {
		return surveyitemnum;
	}
	public void setSurveyitemnum(int surveyitemnum) {
		this.surveyitemnum = surveyitemnum;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	
}
